/***********************************************************************************
* @Funcin que Incluye el registro de la transaccin exitosa
* @parametros: $evento
* @retorno:
* @fecha de creacin: 10/10/2008
* @autor: Ing. Yesenia Moreno de Lang
************************************************************************************
* @fecha modificacin:
* @descripcin:
* @autor:
